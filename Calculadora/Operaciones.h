/** \brief Suma de dos float
 *
 * \param operando1 float  Primer numero ingresado por el usuario
 * \param operando2 float  Segundo numero ingresado por el usuraio
 * \return suma entre los operandos
 *
 */
float suma(float, float );
/** \brief  Resta de dos float
 *
 * \param operando1 float Primer numero ingresado por el usuario
 * \param operando2 float Segundo numero ingresado por el usuraio
 * \return resta de los operandos operando1 - operando2
 *
 */
float resta(float, float );
/** \brief Division de dos float
 *
 * \param operando1 float Primer numero ingresado por el usuario
 * \param operando2 float Segundo numero ingresado por el usuraio
 * \return float
 *
 */
float division(float, float );
/** \brief Multiplicacion de dos float
 *
 * \param operando1 float Primer numero ingresado por el usuario
 * \param operando2 float Segundo numero ingresado por el usuraio
 * \return float
 *
 */
float multiplicacion(float, float );
/** \brief  Factoreo del numero que el usuario haya ingresado
 *  no puede ser negativo, ni decimal.
 * \param numero int Numero ingresado por el usuario
 * \return int devuelve el resultado de la operacion
 *
 */
long long int factorial(int);

